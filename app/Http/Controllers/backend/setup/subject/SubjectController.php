     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'subject' => 'required|unique:subjects,name'
        ]);

        $insertData     = new Subject();

        $insertData->name   = ucwords($request->subject);

        $insertData->save();

        Alert::success('Congrats', ucwords($request->subject) . ' is Successfully Added');
        return redirect()->route('subjects.list');
    }

